"""Axentx Product Example Package.

This package provides a simple `add` function used in the test suite.
"""

def add(a, b):
    """Return the sum of *a* and *b*.

    Parameters
    ----------
    a : int or float
        First operand.
    b : int or float
        Second operand.

    Returns
    -------
    int or float
        The arithmetic sum of *a* and *b*.
    """
    return a + b
